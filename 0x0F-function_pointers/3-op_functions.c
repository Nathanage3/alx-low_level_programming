#include "3-calc.h"
/**
 * op-add - add operation
 * @a: The first number
 * @b: The second number
 * Return: The sum of a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction
 * @a: the first number
 * @b: the second number
 * Return: The difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication
 * @a: The first number
 * @b: The second number
 * Return: the product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * @a: The first number
 * @b: The second number
 * Return: The division of a to b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Return the remainder
 * @a: The first number
 * @b: The Second number
 * Return: The remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
